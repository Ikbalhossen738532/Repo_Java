package String_HW;

public class First_Hw_Datatypes {

    public static void main(String[] args) {
        byte number = 24;
        System.out.println("There is " + number + "hours in a year");

        short maxNumber = 365;
        System.out.println("there is " + maxNumber + "days in a year");

        short totalEmployes = 895;
        System.out.println("there is " + totalEmployes + " employes in an organization ");

        int population = 160000000;
        System.out.println("the population of my country is " + population);

        float interestRate = 4.0f;
        System.out.println("the interest rate is " + interestRate + "%");

        int totalBalance = 12000;
        System.out.println("the account has " + totalBalance + " USD ");

        boolean sunRise = false;
        System.out.println("does the sun raise in the west? answer is " + sunRise);

        char firstWord = 'S';
        char secondWord = 'H';
        System.out.println("the initials of my name is " + firstWord+secondWord);

        String myName = "SHAWAN HASSAN";
        System.out.println("My Full Name is " + myName);





    }
}
